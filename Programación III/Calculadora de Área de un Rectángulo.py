# Calculadora de área de un rectángulo

print("========== Calculadora de Área de un Rectángulo ==========\n")

# Solicitar al usuario que ingrese la altura y la base del rectángulo
altura = float(input("Ingrese la altura del rectángulo: "))
base = float(input("Ingrese la base del rectángulo: "))

# Calcular el área del rectángulo multiplicando la base por la altura
area = base * altura

# Imprimir el área calculada
print("El área del rectángulo es:", area)
